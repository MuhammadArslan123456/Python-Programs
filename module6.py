# Loops

# loop in list
lst = [1,2,3]
for i in lst:
    print(i)

#range in loop
for j in range(3,6):
    print(j)


#while loop
i = 1
while i <= 3:
    print(i)
    i += 1


# nested for loop
for i in range(2):
    for j in range(2):
        print(i, j)

# output nothing
for k in range(3):
    pass
